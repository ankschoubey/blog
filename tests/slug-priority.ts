export const slugSort = (slug1: string, slug2: string) => {
    slug1 = fixSlug(slug1);
    slug2 = fixSlug(slug2);

    const index1 = slugPriority.indexOf(slug1);
    const index2 = slugPriority.indexOf(slug2);
    if(index1 === -1 && index2 === -1) {
        return slug1.localeCompare(slug2);
    }
    if(index1 === -1) {
        return 1;
    }
    if(index2 === -1) {
        return -1;
    }
    return index1 - index2;
}